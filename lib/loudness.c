/* ISO 226 SPL formula for one (af, Lu, Tf) triplet. */
static double iso226_spl(double phon, double af, double Lu, double Tf) {
    double Af = 4.47e-3 * (pow(10.0, 0.025 * phon) - 1.15)
              + pow(0.4 * pow(10.0, (Tf + Lu) / 10.0 - 9.0), af);
    return (10.0 / af) * log10(Af) - Lu + 94.0;
}

