/* Subtract the value at 1 kHz so the contour reads 0 dB there. 1 kHz must be
 * present exactly in `freq`. Returns 0 on success, -1 if 1 kHz is missing. */
static int normalize_at_1k(const double *freq, double *spl, int n) {
    for (int i = 0; i < n; i++) {
        if (fabs(freq[i] - 1000.0) < 1e-6) {
            double ref = spl[i];
            for (int j = 0; j < n; j++) spl[j] -= ref;
            return 0;
        }
    }
    return -1;
}

/* Compute one contour for `model` at `phon`. Allocates the out_freq and out_db
 * arrays (caller frees) and sets out_n. Returns 0 on success, non-zero on error. */
static int loudness_curve(const char *model, double phon,
                          double **out_freq, double **out_db, int *out_n) {
    double *freq = NULL, *db = NULL;
    int n = 0;

    if (strcmp(model, "iso226-2003") == 0 || strcmp(model, "iso226-2023") == 0) {
        int is2023 = (strcmp(model, "iso226-2023") == 0);
        const double *af = is2023 ? ISO226_2023_AF : ISO226_2003_AF;
        const double *lu = is2023 ? ISO226_2023_LU : ISO226_2003_LU;
        const double *tf = is2023 ? ISO226_2023_TF : ISO226_2003_TF;
        n = 29;
        freq = (double*) malloc((size_t) n * sizeof(double));
        db   = (double*) malloc((size_t) n * sizeof(double));
        if (!freq || !db) { free(freq); free(db); return -2; }
        for (int i = 0; i < n; i++) {
            freq[i] = ISO226_FREQ[i];
            db[i]   = iso226_spl(phon, af[i], lu[i], tf[i]);
        }
    } else if (strcmp(model, "fletcher-munson") == 0) {
        n = FM_NFREQ;
        freq = (double*) malloc((size_t) n * sizeof(double));
        db   = (double*) malloc((size_t) n * sizeof(double));
        if (!freq || !db) { free(freq); free(db); return -2; }
        /* Linear interpolation over phon between the two neighbouring rows
         * (extrapolating with the end rows' slope outside [0, 120] phon). */
        int idx = 1;
        while (idx < FM_NLEV - 1 && FM_PHON_LEVELS[idx] < phon) idx++;
        double lo = FM_PHON_LEVELS[idx - 1], hi = FM_PHON_LEVELS[idx];
        double w = (phon - lo) / (hi - lo);
        for (int i = 0; i < n; i++) {
            freq[i] = FM_FREQ[i];
            db[i]   = (1.0 - w) * FM_SPL_TABLE[idx - 1][i] + w * FM_SPL_TABLE[idx][i];
        }
    } else if (strcmp(model, "a-weighting") == 0
            || strcmp(model, "b-weighting") == 0
            || strcmp(model, "c-weighting") == 0) {
        char letter = (char) ('A' + (model[0] - 'a'));   /* 'a'/'b'/'c' -> 'A'/'B'/'C' */
        /* Dense log grid 10 Hz - 20 kHz (256 points) plus an exact 1 kHz point. */
        n = 257;
        freq = (double*) malloc((size_t) n * sizeof(double));
        db   = (double*) malloc((size_t) n * sizeof(double));
        if (!freq || !db) { free(freq); free(db); return -2; }
        double lminf = log10(10.0), lmaxf = log10(20000.0);
        for (int i = 0; i < 256; i++)
            freq[i] = pow(10.0, lminf + (lmaxf - lminf) * (double) i / 255.0);
        freq[256] = 1000.0;
        /* sort ascending (insertion of the single 1 kHz point) */
        for (int i = n - 1; i > 0 && freq[i] < freq[i - 1]; i--) {
            double t = freq[i]; freq[i] = freq[i - 1]; freq[i - 1] = t;
        }
        double R_1k = weighting_R(letter, 1000.0);
        for (int i = 0; i < n; i++) {
            double wdb = 20.0 * log10(weighting_R(letter, freq[i]) / R_1k);
            db[i] = -wdb;   /* equal-loudness equivalent = -weighting */
        }
    } else if (strcmp(model, "robinson-dadson") == 0) {
        return -3;   /* stub in the reference, not supported */
    } else {
        return -1;   /* unknown model */
    }

    if (normalize_at_1k(freq, db, n) != 0) {
        free(freq); free(db);
        return -4;
    }
    *out_freq = freq;
    *out_db   = db;
    *out_n    = n;
    return 0;
}

int loudness_diff_curve(const char *model, double phon, double ref_phon,
                        double **out_freq, double **out_db, int *out_n) {
    double *f1 = NULL, *c1 = NULL, *f2 = NULL, *c2 = NULL;
    int n1 = 0, n2 = 0;
    int rc;

    rc = loudness_curve(model, phon, &f1, &c1, &n1);
    if (rc != 0) return rc;
    rc = loudness_curve(model, ref_phon, &f2, &c2, &n2);
    if (rc != 0) { free(f1); free(c1); return rc; }

    /* Same model -> identical frequency grid; subtract the reference contour. */
    for (int i = 0; i < n1; i++) c1[i] -= c2[i];
    free(f2);
    free(c2);

    *out_freq = f1;
    *out_db   = c1;
    *out_n    = n1;
    return 0;
}

double loudness_db_model(double f_hz, void *ctx_v) {
    const loudness_model_ctx *c = (const loudness_model_ctx *) ctx_v;
    const double *F = c->freq;
    const double *V = c->db;
    int n = c->n;

    if (f_hz < GRID_MIN_HZ || f_hz > GRID_MAX_HZ)
        return SENTINEL_DB;

    double lf = log10(f_hz);
    double l0 = log10(F[0]);
    if (lf < l0) {                                   /* log-linear extrapolation */
        double slope = (V[1] - V[0]) / (log10(F[1]) - l0);
        return V[0] + slope * (lf - l0);
    }
    double ln = log10(F[n - 1]);
    if (lf > ln) {
        double slope = (V[n - 1] - V[n - 2]) / (ln - log10(F[n - 2]));
        return V[n - 1] + slope * (lf - ln);
    }
    int lo = 0, hi = n - 1;                           /* bisect to the interval */
    while (hi - lo > 1) {
        int mid = (lo + hi) / 2;
        if (log10(F[mid]) <= lf) lo = mid; else hi = mid;
    }
    double a = log10(F[lo]), b = log10(F[hi]);
    double w = (b > a) ? (lf - a) / (b - a) : 0.0;
    return V[lo] * (1.0 - w) + V[hi] * w;
}
