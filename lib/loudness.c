/* -------------------------------------------------------------------------
 * IEC A/B/C weighting -- analytic; the phon argument is ignored.
 * ------------------------------------------------------------------------- */
static double weighting_R(char letter, double f) {
    double f2 = f * f;
    double num, den;
    switch (letter) {
    case 'A':
        num = (12194.0 * 12194.0) * (f2 * f2);
        den = (f2 + 20.6 * 20.6)
            * sqrt((f2 + 107.7 * 107.7) * (f2 + 737.9 * 737.9))
            * (f2 + 12194.0 * 12194.0);
        return num / den;
    case 'B':
        num = (12194.0 * 12194.0) * (f2 * f);
        den = (f2 + 20.6 * 20.6) * sqrt(f2 + 158.5 * 158.5)
            * (f2 + 12194.0 * 12194.0);
        return num / den;
    case 'C':
    default:
        num = (12194.0 * 12194.0) * f2;
        den = (f2 + 20.6 * 20.6) * (f2 + 12194.0 * 12194.0);
        return num / den;
    }
}

